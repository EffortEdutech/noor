import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';

const DEFAULT_PROMOTION_BASE = 'https://effortedutech.github.io/noor-cdn/noor-cdn';
const PROMOTION_ROOT = 'content-pipeline/promotion';
const ENV_FILE = join(PROMOTION_ROOT, 'noor-cdn.env.local');
const PROMOTION_FILE = join(PROMOTION_ROOT, 'noor-cdn-promotion.json');
const CHECKLIST_FILE = join(PROMOTION_ROOT, 'noor-cdn-promotion-checklist.md');
const REQUIRED_PATHS = [
  'manifest/noor-content-manifest.json',
  'manifest/noor-content-health.json',
  'metadata/surah-index.json',
  'quran/surahs/001.json',
  'tafseer/demo-tafseer/surahs/001.json',
  'hadith/collections.json'
];

function fail(message) {
  console.error(message);
  process.exit(1);
}

function normalizeBase(rawValue) {
  const value = String(rawValue || process.env.NOOR_CDN_PROMOTION_BASE || DEFAULT_PROMOTION_BASE).trim();
  if (!value) fail('NOOR CDN promotion failed: CDN base URL is required.');
  if (!value.startsWith('https://') && !value.startsWith('http://')) {
    fail(`NOOR CDN promotion failed: CDN base must start with http:// or https://. Received: ${value}`);
  }
  return value.replace(/\/+$/, '');
}

function ensureDirectory(filePath) {
  mkdirSync(dirname(filePath), { recursive: true });
}

function buildEnv(base) {
  return [
    '# NOOR CDN promotion environment handoff',
    '# Generated by pnpm cdn:promote <published-cdn-base>',
    '# Run pnpm cdn:smoke against this same base before applying these values.',
    '',
    'NEXT_PUBLIC_NOOR_DATA_MODE=cdn',
    `NEXT_PUBLIC_NOOR_MANIFEST_CDN_BASE=${base}`,
    `NEXT_PUBLIC_NOOR_QURAN_CDN_BASE=${base}`,
    `NEXT_PUBLIC_NOOR_TAFSEER_CDN_BASE=${base}`,
    `NEXT_PUBLIC_NOOR_HADITH_CDN_BASE=${base}`,
    ''
  ].join('\n');
}

function buildChecklist(base) {
  return [
    '# NOOR CDN Promotion Checklist',
    '',
    `Promotion base: ${base}`,
    '',
    '## Required before applying',
    '',
    `- [ ] Run \`pnpm cdn:smoke ${base}\` and confirm it passes.`,
    '- [ ] Confirm the CDN data repository is public or otherwise reachable by the deployed app.',
    '- [ ] Confirm the content manifest is still demo-only unless production content has been licensed and reviewed.',
    '',
    '## Apply locally',
    '',
    '- [ ] Copy `content-pipeline/promotion/noor-cdn.env.local` to `.env.local`.',
    '- [ ] Restart `pnpm dev`.',
    '- [ ] Open `/settings` and choose External CDN if the runtime cookie is not already set.',
    '- [ ] Open `/learn/quran/1`, `/learn/tafseer`, and `/learn/hadith`.',
    '',
    '## Apply in Vercel',
    '',
    '- [ ] Add the generated NEXT_PUBLIC values in Vercel Project Settings → Environment Variables.',
    '- [ ] Redeploy the latest `main` branch.',
    '- [ ] Confirm `/settings` shows External CDN endpoints.',
    '',
    '## Resolver paths checked by smoke test',
    '',
    ...REQUIRED_PATHS.map((path) => `- ${path}`),
    ''
  ].join('\n');
}

function main() {
  const base = normalizeBase(process.argv[2]);
  const generatedAt = new Date().toISOString();

  const promotion = {
    app: 'NOOR',
    promotionVersion: '0.16.0',
    status: 'manual-apply-after-smoke-pass',
    generatedAt,
    targetBase: base,
    smokeCommand: `pnpm cdn:smoke ${base}`,
    env: {
      NEXT_PUBLIC_NOOR_DATA_MODE: 'cdn',
      NEXT_PUBLIC_NOOR_MANIFEST_CDN_BASE: base,
      NEXT_PUBLIC_NOOR_QURAN_CDN_BASE: base,
      NEXT_PUBLIC_NOOR_TAFSEER_CDN_BASE: base,
      NEXT_PUBLIC_NOOR_HADITH_CDN_BASE: base
    },
    requiredResolverPaths: REQUIRED_PATHS,
    notes: [
      'Run the smoke command before applying these environment values.',
      'Generated files are local handoff artifacts and are ignored by git.',
      'Production content still requires licensing, attribution and scholarly review.'
    ]
  };

  ensureDirectory(PROMOTION_FILE);
  writeFileSync(PROMOTION_FILE, `${JSON.stringify(promotion, null, 2)}\n`, 'utf8');
  writeFileSync(ENV_FILE, buildEnv(base), 'utf8');
  writeFileSync(CHECKLIST_FILE, buildChecklist(base), 'utf8');

  console.log(`NOOR CDN promotion bundle generated for ${base}`);
  console.log(`- ${PROMOTION_FILE}`);
  console.log(`- ${ENV_FILE}`);
  console.log(`- ${CHECKLIST_FILE}`);
  console.log(`Next: run pnpm cdn:smoke ${base}, then copy env values to .env.local or Vercel.`);
}

main();
